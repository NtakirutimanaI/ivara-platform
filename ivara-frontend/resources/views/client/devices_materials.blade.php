3️⃣ Devices & Materials Page

Purpose: Manage all devices or materials registered by the client.

Key Features:

Registered Devices List:

Table with columns: Device/Material Name, Type, Serial Number, Status, Registration Date

Add Device/Material:

Form to register a new device/material

Edit / Delete / Report Stolen:

Buttons for managing each device

Search & Filter:

Filter devices by type, status (active/stolen)

Optional:

Upload documents/photos for devices

Link device to repair records or bookings